/**
    +       --> Cộng
    -       --> Trừ
    *       --> Nhân
    **      --> Lũy thừa
    /       --> Chia
    %       --> Chia lấy số dư
    ++       --> Tăng 1 giá trị số
    --       --> Giảm 1 giá trị số
 */
/**
    + --> Plus
    ---> Minus
    *       --> Human
    **      --> Exponentiation
    /--> Divide
    % --> Divide to get the remainder
    ++ --> Increase the numeric value by 1
    ----> Decrease the numeric value by 1
 */
var a = 5;
var b = 3;

var c = a + b;
console.log(" a + b = " + c);

var d = a % b;
console.log(" a % b = "  + d);

var age = 20;

var nextAge = ++age
console.log("age = " + age);
console.log(" next age = " + nextAge);
